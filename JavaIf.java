class JavaIf{
	public static void main(String[] args){
		String[] name={"nbm","trd","dsf"};
		int roll[]={1,2,3};
		if (name[0]=="nbm"&&roll[0]==1){
			System.out.println("first");
		}else if(name[1]=="trd"&&roll[1]==2){
			System.out.println("second");
	}else if(name[2]=="dsf"&&roll[2]==3){
		System.out.println("third");
	}
	}
}